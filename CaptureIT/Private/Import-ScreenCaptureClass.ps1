<#
.SYNOPSIS
    Creates a ScreenCapture Class 
.DESCRIPTION
    Creates a ScreenCapture Class to take screenshots of the current active window or full screen
.EXAMPLE
    $ReturnObject = New-ScreenCaptureClass
.NOTES
    This was borrowed from Boe Prox in his Take-ScreenShot PowerShell Function    This function has used some of Boe Prox's Take-ScreenShot PowerShell function  
        Name: Take-ScreenShot 
        Author: Boe Prox 
